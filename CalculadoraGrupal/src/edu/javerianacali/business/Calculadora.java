public abstract class Calculadora {

	public abstract int sumar(int num1, int num2);

	public abstract int restar(int num1, int num2);

	public abstract int multiplicar(int num1, int num2);

	public abstract int dividir(int num1, int num2);

	public abstract int potencia(int num1, int num2);

	public abstract Double fibonacci(int num1, int num2);
}
